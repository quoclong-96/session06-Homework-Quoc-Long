let physic = Number(prompt("Nhập điểm môn Vật lý"));
let chemistry = Number(prompt("Nhập điểm môn Hóa học"));
let biology = Number(prompt("Nhập điểm môn Sinh học"));

let sum = physic + chemistry + biology;
let average = sum / 3;

console.log("Tổng điểm 3 môn là: ", sum);
console.log("Điểm trung bình 3 môn là: ", average);
